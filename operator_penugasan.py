a = int(input("Inputkan nilai a: "))

print("Nilai a = %d" % a)

a += 5 # a = a + 5 = 2 + 5 = 7
print("Nilai setelah ditambah 5:")
print("a = %d" % a)

a -= 3 # a = a - 3 = 7 - 3 = 4

a *= 10 # a = a * 10 = 4 * 10 = 40

a /= 4 # a = a / 4 = 40 / 4 = 10

a %= 2 # a = a % 2 = 10 % 2 = 0

a **= 10 # a = a ** 10 = 0 ** 10 = 0

print("Nilai a adalah %d" % a)

